// Classes, structs, enums, interfaces, delegates are called as types and 
// fields, properties, constructors , methods etc. that are in a type are called as type members.

// There are five different type member access modifiers
// Private => Only within the containing class (default)
// Protected => Within the containing types and types derived from the containing types
// Internal => Anywhere within the same assembly (same dll)
// Protected Internal => Anywhere within the same assembly and from within a derived class
// in any other assembly (different dll)
// Public => Available Everywhere
// Private Protected => Only within the containing class or a class that derives from the containing class but only in the same assembly

// Type members can have all the access modifiers whereas types can only have 2 access modifiers 
